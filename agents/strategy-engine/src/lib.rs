    #[test]
    fn test_no_signal_on_first_ingest() {
        let mut engine = StrategyEngine::new();
        let line = make_line("WC2026-001", MarketType::MatchWinner, "TeamA", 2.10);
        let signal = engine.ingest(line);
        assert!(signal.is_none(), "First ingest should not trigger a signal");
    }

    #[test]
    fn test_signal_on_significant_shift() {
        let mut engine = StrategyEngine::new();
        let t1 = chrono::Utc::now();
        let t2 = t1 + chrono::Duration::seconds(30);

        let line1 = OddsLine {
            match_id: "WC2026-001".into(),
            market_type: MarketType::MatchWinner,
            outcome: "TeamA".into(),
            odds: dec!(2.50),
            timestamp: t1,
            bookmaker: "BookA".into(),
            signature: None,
        };
        engine.ingest(line1);

        let line2 = OddsLine {
            match_id: "WC2026-001".into(),
            market_type: MarketType::MatchWinner,
            outcome: "TeamA".into(),
            odds: dec!(2.00), // 20% drop = 2000 bps
            timestamp: t2,
            bookmaker: "BookA".into(),
            signature: None,
        };
        let signal = engine.ingest(line2);
        assert!(signal.is_some(), "Large shift should trigger signal");
        let s = signal.unwrap();
        assert_eq!(s.action, TradeAction::Back);
        assert!(s.confidence > 0.8);
    }

    #[test]
    fn test_cooldown_suppression() {
        let mut engine = StrategyEngine::new();
        let t = chrono::Utc::now();

        let line1 = OddsLine {
            match_id: "WC2026-002".into(),
            market_type: MarketType::AsianHandicap,
            outcome: "TeamB -1".into(),
            odds: dec!(1.95),
            timestamp: t,
            bookmaker: "BookA".into(),
            signature: None,
        };
        engine.ingest(line1);

        // Trigger signal with large shift
        let line2 = OddsLine {
            match_id: "WC2026-002".into(),
            market_type: MarketType::AsianHandicap,
            outcome: "TeamB -1".into(),
            odds: dec!(1.60),
            timestamp: t + chrono::Duration::seconds(10),
            bookmaker: "BookA".into(),
            signature: None,
        };
        let sig1 = engine.ingest(line2);
        assert!(sig1.is_some());

        // Immediate follow-up should be suppressed by cooldown
        let line3 = OddsLine {
            match_id: "WC2026-002".into(),
            market_type: MarketType::AsianHandicap,
            outcome: "TeamB -1".into(),
            odds: dec!(1.55),
            timestamp: t + chrono::Duration::seconds(15),
            bookmaker: "BookA".into(),
            signature: None,
        };
        let sig2 = engine.ingest(line3);
        assert!(sig2.is_none(), "Cooldown should suppress rapid signals");
    }

