/// FANatic Alpha — Strategy Engine
///
/// Decision logic for autonomous World Cup trading agent.
/// Monitors odds shifts from TxLINE feeds and generates trade signals
/// based on sharp movement detection, arbitrage cross-market signals,
/// and time-decay momentum scoring.

