/// Market types available in the TxLINE odds feed for World Cup matches.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum MarketType {
    #[serde(rename = "match_winner")]
    MatchWinner,
    #[serde(rename = "total_goals")]
    TotalGoals,
    #[serde(rename = "both_teams_to_score")]
    BothTeamsToScore,
    #[serde(rename = "correct_score")]
    CorrectScore,
    #[serde(rename = "over_under_2_5")]
    OverUnder25,
    #[serde(rename = "asian_handicap")]
    AsianHandicap,
    #[serde(rename = "first_goal_scorer")]
    FirstGoalScorer,
    #[serde(rename = "half_time_full_time")]
    HalfTimeFullTime,
}

impl MarketType {
    /// Returns the minimum odds shift (in basis points) to qualify as "significant"
    /// for this market type. More liquid markets require larger shifts.
    pub fn significance_threshold_bps(&self) -> u32 {
        match self {
            MarketType::MatchWinner => 15,       // 0.15% shift = significant
            MarketType::TotalGoals => 12,
            MarketType::BothTeamsToScore => 18,
            MarketType::CorrectScore => 25,      // thinner, needs bigger move
            MarketType::OverUnder25 => 10,       // very liquid
            MarketType::AsianHandicap => 8,      // extremely liquid
            MarketType::FirstGoalScorer => 30,   // erratic, filter noise
            MarketType::HalfTimeFullTime => 20,
        }
    }
}

/// An individual odds line from the TxLINE feed.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct OddsLine {
    pub match_id: String,
    pub market_type: MarketType,
    pub outcome: String,
    pub odds: Decimal,
    pub timestamp: DateTime<Utc>,
    pub bookmaker: String,
    #[serde(default)]
    pub signature: Option<String>,
}

/// Historical snapshot of an odds line for comparison.
#[derive(Debug, Clone)]
pub struct OddsSnapshot {
    pub line: OddsLine,
    pub recorded_at: DateTime<Utc>,
    pub source: String,
}

/// A detected odds shift event.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct OddsShiftEvent {
    pub match_id: String,
    pub market_type: MarketType,
    pub outcome: String,
    pub previous_odds: Decimal,
    pub current_odds: Decimal,
    pub shift_bps: u32,
    pub direction: ShiftDirection,
    pub detected_at: DateTime<Utc>,
    pub bookmaker: String,
    pub severity: ShiftSeverity,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum ShiftDirection {
    #[serde(rename = "up")]
    Up,
    #[serde(rename = "down")]
    Down,
    #[serde(rename = "stable")]
    Stable,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum ShiftSeverity {
    #[serde(rename = "minor")]
    Minor,
    #[serde(rename = "notable")]
    Notable,
    #[serde(rename = "significant")]
    Significant,
    #[serde(rename = "critical")]
    Critical,
}

/// A trade signal generated by the strategy engine.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct TradeSignal {
    pub signal_id: String,
    pub match_id: String,
    pub market_type: MarketType,
    pub outcome: String,
    pub action: TradeAction,
    pub odds: Decimal,
    pub stake_recommendation: Decimal,
    pub confidence: f64,
    pub trigger: SignalTrigger,
    pub generated_at: DateTime<Utc>,
    pub expires_at: DateTime<Utc>,
    pub reasoning: String,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum TradeAction {
    #[serde(rename = "back")]
    Back,
    #[serde(rename = "lay")]
    Lay,
    #[serde(rename = "no_action")]
    NoAction,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SignalConfig {
    /// Minimum odds shift in basis points to trigger a signal.
    pub min_shift_bps: u32,
    /// Maximum stake as a fraction of bankroll.
    pub max_stake_fraction: Decimal,
    /// Signal cooldown in seconds (prevent duplicate signals).
    pub cooldown_secs: u64,
    /// Minimum confidence required to execute.
    pub min_confidence: f64,
    /// Enable cross-market arbitrage detection.
    pub enable_arbitrage: bool,
    /// Momentum window in seconds for trend detection.
    pub momentum_window_secs: u64,
    /// Minimum number of data points for momentum calculation.
    pub momentum_min_samples: usize,
    /// Odds reversal threshold — if odds swing back this many bps within cooldown, flag reversal.
    pub reversal_threshold_bps: u32,
}

impl Default for SignalConfig {
    fn default() -> Self {
        Self {
            min_shift_bps: 10,
            max_stake_fraction: dec!(0.05),
            cooldown_secs: 60,
            min_confidence: 0.65,
            enable_arbitrage: true,
            momentum_window_secs: 300,
            momentum_min_samples: 5,
            reversal_threshold_bps: 20,
        }
    }
}

