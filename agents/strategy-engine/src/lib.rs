#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum SignalTrigger {
    #[serde(rename = "sharp_odds_shift")]
    SharpOddsShift {
        shift_bps: u32,
        direction: ShiftDirection,
        previous_odds: Decimal,
    },
    #[serde(rename = "cross_market_arbitrage")]
    CrossMarketArbitrage {
        spread_bps: u32,
        best_odds: Decimal,
        worst_odds: Decimal,
        best_bookmaker: String,
    },
    #[serde(rename = "momentum_breakout")]
    MomentumBreakout {
        momentum_score: f64,
        sample_count: usize,
    },
    #[serde(rename = "odds_reversal")]
    OddsReversal {
        reversal_bps: u32,
        original_direction: ShiftDirection,
    },
}

