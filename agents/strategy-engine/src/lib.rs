/// The main strategy engine that processes odds data and produces trade signals.
pub struct StrategyEngine {
    config: SignalConfig,
    /// Recent odds snapshots keyed by (match_id, market_type, outcome).
    history: HashMap<String, Vec<OddsSnapshot>>,
    /// Recently emitted signal hashes to enforce cooldown.
    recent_signals: HashMap<String, DateTime<Utc>>,
}

impl StrategyEngine {
    /// Create a new strategy engine with default configuration.
    pub fn new() -> Self {
        Self::with_config(SignalConfig::default())
    }

    /// Create a new strategy engine with custom configuration.
    pub fn with_config(config: SignalConfig) -> Self {
        Self {
            config,
            history: HashMap::new(),
            recent_signals: HashMap::new(),
        }
    }

    /// Build a lookup key for deduplication.
    fn history_key(match_id: &str, market_type: MarketType, outcome: &str) -> String {
        format!("{}|{}|{}", match_id, market_type_string(market_type), outcome)
    }

    /// Ingest a new odds line and check if it generates a trade signal.
    pub fn ingest(&mut self, line: OddsLine) -> Option<TradeSignal> {
        let key = Self::history_key(&line.match_id, line.market_type, &line.outcome);
        let now = line.timestamp;

        // Record this line
        let snapshot = OddsSnapshot {
            line: line.clone(),
            recorded_at: now,
            source: "TxLINE_SSE".into(),
        };

        let previous = self.history.entry(key.clone()).or_default();

        // Clean old entries outside momentum window
        let cutoff = now - chrono::Duration::seconds(self.config.momentum_window_secs as i64);
        previous.retain(|s| s.recorded_at >= cutoff);

        // Detect shift if we have a prior snapshot
        let signal = if let Some(last) = previous.last() {
            self.detect_shift(last, &line, now)
        } else {
            None
        };

        previous.push(snapshot);

        // Enforce max history per key
        if previous.len() > 1000 {
            previous.drain(0..previous.len() - 500);
        }

        signal
    }

    /// Core shift detection logic.
    fn detect_shift(
        &mut self,
        previous: &OddsSnapshot,
        current: &OddsLine,
        now: DateTime<Utc>,
    ) -> Option<TradeSignal> {
        let prev_odds = previous.line.odds;
        let curr_odds = current.odds;

        if prev_odds.is_zero() || curr_odds.is_zero() {
            warn!("Zero odds detected — skipping signal generation");
            return None;
        }

        // Calculate shift in basis points (1 bp = 0.0001 = 0.01%)
        // shift_bps = |(current - previous) / previous| * 10000
        let delta = curr_odds - prev_odds;
        let abs_delta = delta.abs();
        let shift_bps = (abs_delta / prev_odds * Decimal::from(10000u32))
            .round()
            .to_u32()
            .unwrap_or(0);

        let direction = if delta > Decimal::ZERO {
            ShiftDirection::Up
        } else if delta < Decimal::ZERO {
            ShiftDirection::Down
        } else {
            ShiftDirection::Stable
        };

        // Check against market-specific significance threshold
        let significance_threshold = current.market_type.significance_threshold_bps();
        let severity = classify_severity(shift_bps, significance_threshold);

        if severity == ShiftSeverity::Minor && shift_bps < self.config.min_shift_bps {
            debug!(
                match_id=%current.match_id, market=?current.market_type,
                outcome=%current.outcome, shift_bps,
                "Shift below minimum threshold — no signal"
            );
            return None;
        }

        // Cooldown check
        let cooldown_key = Self::history_key(&current.match_id, current.market_type, &current.outcome);
        if let Some(last_signal_time) = self.recent_signals.get(&cooldown_key) {
            let elapsed = (now - *last_signal_time).num_seconds();
            if elapsed < self.config.cooldown_secs as i64 {
                debug!(match_id=%current.match_id, elapsed, "Cooldown active — suppressing signal");
                return None;
            }
        }

        // Check for reversal (odds swung opposite direction after a recent signal)
        if let Some(last_signal_time) = self.recent_signals.get(&cooldown_key) {
            let reversal_bps = (delta.abs() / prev_odds * Decimal::from(10000u32))
                .round()
                .to_u32()
                .unwrap_or(0);
            if reversal_bps >= self.config.reversal_threshold_bps
                && (now - *last_signal_time).num_seconds() < self.config.cooldown_secs as i64 * 3
            {
                info!(
                    match_id=%current.match_id, reversal_bps,
                    "Odds reversal detected — potential mean reversion opportunity"
                );
                // Still emit the signal — reversal is itself a signal
            }
        }

        // Determine trade action: back on odds drop, lay on odds rise
        let action = match direction {
            ShiftDirection::Up => TradeAction::Lay,
            ShiftDirection::Down => TradeAction::Back,
            ShiftDirection::Stable => TradeAction::NoAction,
        };

        // Calculate confidence based on shift magnitude and market type
        let confidence = calculate_confidence(shift_bps, significance_threshold, &current.market_type);
        if confidence < self.config.min_confidence {
            debug!(confidence, min_confidence=%self.config.min_confidence, "Confidence below threshold");
            return None;
        }

        // Calculate recommended stake using Kelly-adjacent sizing
        let stake = self.calculate_stake(prev_odds, curr_odds, confidence);

        // Generate signal ID
        let signal_id = format!(
            "FAN-{}-{}-{}-{}",
            chrono::Utc::now().format("%Y%m%d%H%M%S"),
            &current.match_id[..current.match_id.len().min(6)],
            market_type_abbrev(current.market_type),
            uuid::Uuid::new_v4().to_string().split('-').next().unwrap_or("0")
        );

        let signal = TradeSignal {
            signal_id,
            match_id: current.match_id.clone(),
            market_type: current.market_type,
            outcome: current.outcome.clone(),
            action,
            odds: curr_odds,
            stake_recommendation: stake,
            confidence,
            trigger: SignalTrigger::SharpOddsShift {
                shift_bps,
                direction,
                previous_odds: prev_odds,
            },
            generated_at: now,
            expires_at: now + chrono::Duration::seconds(300), // 5-minute validity
            reasoning: format!(
                "{} shift of {} bps detected on {} market for outcome '{}'. \
                 Previous odds: {:.2}, current: {:.2}. Confidence: {:.1}%.",
                severity_label(severity), shift_bps,
                market_type_string(current.market_type),
                current.outcome, prev_odds, curr_odds,
                confidence * 100.0
            ),
        };

        // Record signal for cooldown
        self.recent_signals.insert(cooldown_key, now);

        info!(
            signal_id=%signal.signal_id, match_id=%current.match_id,
            action=?signal.action, odds=%curr_odds, confidence=%confidence,
            "Trade signal generated"
        );

        Some(signal)
    }

    /// Calculate recommended stake using a modified Kelly criterion approximation.
    /// stake = bankroll * fraction * confidence * edge_factor
    fn calculate_stake(
        &self,
        prev_odds: Decimal,
        curr_odds: Decimal,
        confidence: f64,
    ) -> Decimal {
        let edge_bps = ((curr_odds - prev_odds) / prev_odds * Decimal::from(10000u32))
            .abs()
            .round()
            .to_u32()
            .unwrap_or(0) as f64;

        // Edge factor: 0 at 0 bps, asymptoting to 1.0 at high bps
        let edge_factor = 1.0 - (-edge_bps / 50.0).exp();

        // Kelly fraction scaled by confidence and edge
        let kelly = self.config.max_stake_fraction.to_f64().unwrap_or(0.05) * confidence * edge_factor;

        // Clamp between 1% and max_stake_fraction of bankroll
        let clamped = kelly.clamp(0.01, self.config.max_stake_fraction.to_f64().unwrap_or(0.05));

        Decimal::from_f64_retain(clamped).unwrap_or(dec!(0.01))
    }

    /// Detect cross-market arbitrage signals by comparing odds across bookmakers
    /// for the same match/market/outcome.
    pub fn detect_arbitrage(
        &self,
        lines: &[OddsLine],
    ) -> Vec<TradeSignal> {
        if !self.config.enable_arbitrage {
            return vec![];
        }

        let mut signals = Vec::new();

        // Group by (match_id, market_type, outcome)
        let mut grouped: HashMap<String, Vec<&OddsLine>> = HashMap::new();
        for line in lines {
            let key = Self::history_key(&line.match_id, line.market_type, &line.outcome);
            grouped.entry(key).or_default().push(line);
        }

        for (_key, group) in grouped {
            if group.len() < 2 {
                continue;
            }

            // Find max and min odds across bookmakers
            let max_line = group
                .iter()
                .max_by(|a, b| a.odds.cmp(&b.odds))
                .unwrap();
            let min_line = group
                .iter()
                .min_by(|a, b| a.odds.cmp(&b.odds))
                .unwrap();

            let spread = max_line.odds - min_line.odds;
            if spread.is_zero() {
                continue;
            }

            let spread_bps = (spread / min_line.odds * Decimal::from(10000u32))
                .round()
                .to_u32()
                .unwrap_or(0);

            // Arbitrage threshold: >50 bps spread is actionable
            if spread_bps < 50 {
                continue;
            }

            let signal = TradeSignal {
                signal_id: format!("ARB-{}", uuid::Uuid::new_v4()),
                match_id: max_line.match_id.clone(),
                market_type: max_line.market_type,
                outcome: max_line.outcome.clone(),
                action: TradeAction::Back,
                odds: max_line.odds,
                stake_recommendation: dec!(0.03), // 3% on arb
                confidence: 0.85,
                trigger: SignalTrigger::CrossMarketArbitrage {
                    spread_bps,
                    best_odds: max_line.odds,
                    worst_odds: min_line.odds,
                    best_bookmaker: max_line.bookmaker.clone(),
                },
                generated_at: chrono::Utc::now(),
                expires_at: chrono::Utc::now() + chrono::Duration::seconds(120),
                reasoning: format!(
                    "Cross-market arbitrage: {} bps spread on {} {}. \
                     Best: {} @ {:.2}, Worst: {} @ {:.2}",
                    spread_bps, max_line.match_id, max_line.outcome,
                    max_line.bookmaker, max_line.odds,
                    min_line.bookmaker, min_line.odds
                ),
            };

            signals.push(signal);
        }

        signals
    }

    /// Detect momentum signals from historical odds trends.
    pub fn detect_momentum(&self, match_id: &str, market_type: MarketType) -> Option<f64> {
        let mut all_lines: Vec<&OddsSnapshot> = self
            .history
            .iter()
            .filter(|(k, _)| k.starts_with(match_id))
            .flat_map(|(_, v)| v.iter())
            .collect();

        if all_lines.len() < self.config.momentum_min_samples {
            return None;
        }

        all_lines.sort_by_key(|s| s.recorded_at);

        // Simple linear momentum: slope of odds over time
        let first = all_lines.first().unwrap();
        let last = all_lines.last().unwrap();
        let duration_secs = (last.recorded_at - first.recorded_at).num_seconds() as f64;

        if duration_secs < 1.0 {
            return None;
        }

        let odds_change = (last.line.odds - first.line.odds)
            .to_f64()
            .unwrap_or(0.0);
        let momentum = odds_change / duration_secs;

        Some(momentum)
    }

    /// Clear all internal state.
    pub fn reset(&mut self) {
        self.history.clear();
        self.recent_signals.clear();
    }

    /// Return current state size for monitoring.
    pub fn state_size(&self) -> (usize, usize) {
        (self.history.len(), self.recent_signals.len())
    }
}

impl Default for StrategyEngine {
    fn default() -> Self {
        Self::new()
    }
}

